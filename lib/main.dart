        GoRoute(
          path: '/popular-cars',
          name: 'popular-cars',
          parentNavigatorKey: rootNavigatorKey,
          builder: (context, state) {
            final governorate = state.uri.queryParameters['governorate'] ?? '';
            return PopularCarsScreen(governorateId: governorate);
          },
        ),
        GoRoute(
          path: '/favorites',
          name: 'favorites',
          parentNavigatorKey: rootNavigatorKey,
          builder: (context, state) => const FavoritesScreen(),
        ),
        // Bottom navigation shell route
        ShellRoute(
          navigatorKey: shellNavigatorKey,
          builder: (context, state, child) {
            // Determine active page for bottom navigation
            int currentIndex = 0;
            final location = state.matchedLocation;

            switch (location) {
              case '/home':
                currentIndex = 0;
                break;
              case '/explore':
                currentIndex = 1;
                break;
              case '/my-bookings':
                currentIndex = 2;
                break;
              case '/chat':
                currentIndex = 3;
                break;
              case '/profile':
                currentIndex = 4;
                break;
            }

            return AppBottomNavigation(
              currentIndex: currentIndex,
              child: child,
            );
          },
          routes: [
            GoRoute(
              path: '/home',
              name: 'home',
              builder: (context, state) => const HomeScreen(),
            ),
            GoRoute(
              path: '/explore',
              name: 'explore',
              builder: (context, state) => const ExploreScreen(),
            ),

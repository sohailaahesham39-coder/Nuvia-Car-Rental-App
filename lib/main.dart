            colorScheme: ColorScheme.fromSeed(
              seedColor: const Color(0xFF1E88E5),
              brightness: Brightness.dark,
              primary: const Color(0xFF1E88E5),
              secondary: const Color(0xFFFFA726),
              surface: Colors.grey[900]!,
            ),
            useMaterial3: true,

              path: '/my-bookings',
              name: 'my-bookings',
              builder: (context, state) => const MyBookingsScreen(),
            ),
            GoRoute(
              path: '/chat',
              name: 'chat',
              builder: (context, state) => const ChatScreen(),
            ),
            GoRoute(
              path: '/profile',
              name: 'profile',
              builder: (context, state) => const ProfileScreen(),
            ),
          ],
        ),
      ],
      // Route protection and redirection
      redirect: (context, state) async {
        final authProvider = Provider.of<AuthProvider>(context, listen: false);
        final publicRoutes = ['/', '/onboarding', '/sign-in', '/sign-up'];

        // Redirect to onboarding if first launch
        final prefs = await SharedPreferences.getInstance();
        final isFirstLaunch = prefs.getBool('first_launch') ?? true;
        if (isFirstLaunch && state.matchedLocation == '/') {
          return '/onboarding';
        }

        // Redirect to sign-in if not authenticated for protected routes
        if (!authProvider.isAuthenticated && !publicRoutes.contains(state.matchedLocation)) {
          return '/sign-in';
        }

        // Redirect to complete profile if needed
        if (authProvider.isAuthenticated) {
          final isProfileComplete = await authProvider.isProfileComplete();
          if (!isProfileComplete &&
              state.matchedLocation != '/complete-profile' &&
              !publicRoutes.contains(state.matchedLocation)) {
            return '/complete-profile';
          }
        }

        return null;
      },
    );

